void InitMap()
{
	
	int src_handle_map = LoadGraph(MAP_CHIP_1);

	for (int height = 0; height < MAP_CHIP_NUM; height++)
	{
		for (int width = 0; width < MAP_CHIP_NUM; width++)
		{
			int start_x = 0, start_y = 0;

			if (MapLine_Main[height][width] == WOOD)
			{
				start_x = 0;
				start_y = 0;
				mapInfo[height][width].isUse = true;
				mapInfo[height][width].kind = WOOD;
			}
			else if(MapLine_Main[height][width] == ROCK)
			{
				start_x = MAP_PATH_MEDIUM;
				start_y = 0;
				mapInfo[height][width].isUse = true;
				mapInfo[height][width].kind = ROCK;
			}

			mapInfo[height][width].handle = DerivationGraph(start_x, start_y,
				MAP_PATH_MEDIUM,
				MAP_PATH_MEDIUM,
				src_handle_map);
		}
	}

	if (save_loop == 0)
	{
		for (int height = 0; height < MAP_CHIP_NUM; height++)
		{
			for (int width = 0; width < MAP_CHIP_NUM; width++)
			{
				mapInfo_save[height][width].x = mapInfo[height][width].x;
				mapInfo_save[height][width].y = mapInfo[height][width].y;
				mapInfo_save[height][width].kind = mapInfo[height][width].kind;
				mapInfo_save[height][width].isUse = mapInfo[height][width].isUse;
			}
		}
		save_loop++;		//àÍìxÇµÇ©í Ç≥Ç»Ç¢èàóù
	}

}

